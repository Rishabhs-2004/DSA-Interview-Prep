import java.util.*;
public class Relational {
    public static void main(String[] args) {
        Scanner sc = new Scanner (System.in);
             
        
        int a= 10;
        int b= 20;

        System.out.println(a==b); // equai operator.
        System.out.println(a>b); // greater operator.
        System.out.println(a>=b); // greater than equal operator.
        System.out.println(a<b); // less operator.
        System.out.println(a<=b); // less than equal operator.
        System.out.println(a!=b); //  not equai operator.

    }
}
